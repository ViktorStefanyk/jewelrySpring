package com.web.sj.domain;

public class Gemstone {
	
	public Integer gemstoneId;
	public String gemstoneName;
	public String gemstoneLink;
	public Integer gemstoneQuantity;
	
	public Gemstone() {
		super();
	}

	public Integer getGemstoneId() {
		return gemstoneId;
	}

	public void setGemstoneId(Integer gemstoneId) {
		this.gemstoneId = gemstoneId;
	}

	public String getGemstoneName() {
		return gemstoneName;
	}

	public void setGemstoneName(String gemstoneName) {
		this.gemstoneName = gemstoneName;
	}

	public String getGemstoneLink() {
		return gemstoneLink;
	}

	public void setGemstoneLink(String gemstoneLink) {
		this.gemstoneLink = gemstoneLink;
	}

	public Integer getGemstoneQuantity() {
		return gemstoneQuantity;
	}

	public void setGemstoneQuantity(Integer gemstoneQuantity) {
		this.gemstoneQuantity = gemstoneQuantity;
	}	
	

}
