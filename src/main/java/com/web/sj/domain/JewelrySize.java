package com.web.sj.domain;

public class JewelrySize {
	
	public Integer sizeId;
	public Float sizeName;
	public Integer sizeQuantity;
	
	public JewelrySize() {
		super();
	}

	public Integer getSizeId() {
		return sizeId;
	}

	public void setSizeId(Integer sizeId) {
		this.sizeId = sizeId;
	}

	public Float getSizeName() {
		return sizeName;
	}

	public void setSizeName(Float sizeName) {
		this.sizeName = sizeName;
	}

	public Integer getSizeQuantity() {
		return sizeQuantity;
	}

	public void setSizeQuantity(Integer sizeQuantity) {
		this.sizeQuantity = sizeQuantity;
	}

}
